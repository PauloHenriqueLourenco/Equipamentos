    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
        if (statusBotaoFechar == 0) {
            JOptionPane.showMessageDialog(null, "Preencha os campos 'Data Fechamaneto' e 'Solução' liberados e depois clique novamente em Fechar Serviço");
            bloquear();
            edtData_fechamento.setEnabled(true);
            edtSolucao.setEnabled(true);
            statusBotaoFechar = 1;
        } else if (statusBotaoFechar == 1) {
            o.setData_fechamento(edtData_fechamento.getText());
            o.setSolucao(edtSolucao.getText());
            int resp=JOptionPane.showConfirmDialog(null,"Deseja realmente fechar a Ordem de Serviço do componente "+cbComponente.getSelectedItem()+""+" ?");
            if(resp==JOptionPane.YES_OPTION){
                int erro=ctrlO.fecharOrdemServico(o, num, cod);
                if(erro==0){
                    JOptionPane.showMessageDialog(null,"Ordem de Serviço fechada com sucesso !!");
                    limpar();
                    resetar();
                }else
                JOptionPane.showMessageDialog(null,"Falha no fechamento");
            }
            statusBotaoFechar = 0;
        }
    }//GEN-LAST:event_btnFecharActionPerformed

