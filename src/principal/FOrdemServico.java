    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        o.setNum_comp(ctrlO.encontrarCodigo(cbComponente.getSelectedItem()+"", "componente"));
        o.setCodigo_defeito(ctrlO.encontrarCodigo(cbDefeito.getSelectedItem()+"", "defeito"));
        o.setData_abertura(edtData_abertura.getText());
        o.setEstado(edtEstado.getText());
        int erro=ctrlO.abrirOrdemServico (o);
        if(erro==0){
            JOptionPane.showMessageDialog(null,"Ordem de Servico aberta com sucesso!");
            limpar();
            resetar();
        }else
        JOptionPane.showMessageDialog(null,"Falha na abertura da ordem de servico!");
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        o.setNum_comp(ctrlO.encontrarCodigo(cbComponente.getSelectedItem()+"", "componente"));
        o.setCodigo_defeito(ctrlO.encontrarCodigo(cbDefeito.getSelectedItem()+"", "defeito"));
        o.setData_abertura(edtData_abertura.getText());
        o.setEstado(edtEstado.getText());
        int resp=JOptionPane.showConfirmDialog(null,"Deseja realmente alterar os dados da Ordem de Serviço do componente "+cbComponente.getSelectedItem()+""+" ?");
        if(resp==JOptionPane.YES_OPTION){
            int erro=ctrlO.alterarOrdemServico(o, num, cod);
            if(erro==0){
                JOptionPane.showMessageDialog(null,"Alteração concluída com êxito");
                limpar();
                resetar();
            }else
            JOptionPane.showMessageDialog(null,"Falha na alteração");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        o.setNum_comp(ctrlO.encontrarCodigo(cbComponente.getSelectedItem()+"", "componente"));
        o.setCodigo_defeito(ctrlO.encontrarCodigo(cbDefeito.getSelectedItem()+"", "defeito"));
        int resp=JOptionPane.showConfirmDialog(null,"Deseja realmente excluir a Ordem de Serviço do componente "+cbComponente.getSelectedItem()+" ?");
        if (resp==JOptionPane.YES_OPTION){
            int erro=ctrlO.excluirOrdemServico (o);
            if(erro==0){
                JOptionPane.showMessageDialog(null,"Exclusão concluída com êxito");
                limpar();
                resetar();
            }else
            JOptionPane.showMessageDialog(null,"Falha na exclusão");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        num = ctrlO.encontrarCodigo(cbComponente.getSelectedItem()+"", "componente");
        cod = ctrlO.encontrarCodigo(cbDefeito.getSelectedItem()+"", "defeito");
        int rc=ctrlO.consultar(num, cod, o);
        if (rc!=0)
        JOptionPane.showMessageDialog(null,"Ordem de Servico não encontrado !");
        else {
            cbComponente.setSelectedItem(o.getNum_comp());
            cbDefeito.setSelectedItem(o.getCodigo_defeito());
            edtData_abertura.setText(o.getData_abertura());
            edtEstado.setText(o.getEstado());
            edtData_fechamento.setText(o.getData_fechamento());
            edtSolucao.setText(o.getSolucao());
            liberar();
            btnIncluir.setEnabled(false);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

