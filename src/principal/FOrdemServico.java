    public void carregarCb(String tabela, JComboBox cb) {
        ResultSet rs;
        rs=ctrlO.encontrarCodigos(tabela); 
        try { 
            while(rs.next()){ 
                int cod = rs.getInt(1);
                String nome = ctrlO.encontrarNome(cod, tabela);
                
                cb.addItem(nome); 
                cb.updateUI(); 

            } 
        } catch (SQLException ex) { 
                Logger.getLogger(FMontagem.class.getName()).log(Level.SEVERE, null, ex); 
        }
    }
    
