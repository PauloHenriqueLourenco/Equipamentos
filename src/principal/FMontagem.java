    public void carregarCb(String tabela, JComboBox cb) {
        ResultSet rs;
        rs=ctrlM.encontrarNum_comp(tabela); 
        try { 
            while(rs.next()){ 
                int cod = rs.getInt(1);
                String fabricante = ctrlM.encontrarFabricante(cod);
                String descricao = ctrlM.encontrarDescricao(cod);
                
                cb.addItem(" "+fabricante+" -"+descricao+"."); 
                cb.updateUI(); 

            } 
        } catch (SQLException ex) { 
                Logger.getLogger(FMontagem.class.getName()).log(Level.SEVERE, null, ex); 
        }
    }
    
    public String pegarFabricante (JComboBox cb) {
        String texto = cb.getSelectedItem()+"";  
        int posicao1 = texto.indexOf(" ") + 1;  
        int posicao2 = texto.indexOf(" -", posicao1);  
        String selecao = texto.substring(posicao1, posicao2);
        
        return selecao;
    }
    
    public String pegarDescricao (JComboBox cb) {
        String texto = cb.getSelectedItem()+"";  
        int posicao1 = texto.indexOf("-") + 1;  
        int posicao2 = texto.indexOf(".", posicao1);  
        String selecao = texto.substring(posicao1, posicao2);
        
        return selecao;
    }
    
