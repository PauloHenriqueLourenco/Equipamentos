    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        m.setCodigo(Integer.parseInt(edtCodigo.getText()));
        m.setCod_monitor(ctrlM.encontrarCodigo(pegarFabricante(cbMonitor),"monitor",pegarDescricao(cbMonitor)));
        m.setCod_teclado(ctrlM.encontrarCodigo(pegarFabricante(cbTeclado),"teclado",pegarDescricao(cbTeclado)));
        m.setCod_gabinete(ctrlM.encontrarCodigo(pegarFabricante(cbGabinete),"gabinete",pegarDescricao(cbGabinete)));
        m.setCod_impressora(ctrlM.encontrarCodigo(pegarFabricante(cbImpressora),"impressora",pegarDescricao(cbImpressora)));
        m.setCod_scanner(ctrlM.encontrarCodigo(pegarFabricante(cbScanner),"scanner",pegarDescricao(cbScanner)));
        m.setCod_mouse(ctrlM.encontrarCodigo(pegarFabricante(cbMouse),"mouse",pegarDescricao(cbMouse)));
        int erro=ctrlM.inserirMaquina(m);
        if(erro==0){
            JOptionPane.showMessageDialog(null,"Inclusão concluída com êxito!");
            limpar();
            resetar();
        }else
        JOptionPane.showMessageDialog(null,"Falha na inclusão !");
    }//GEN-LAST:event_btnIncluirActionPerformed

