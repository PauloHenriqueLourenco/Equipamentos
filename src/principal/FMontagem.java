        m.setCod_teclado(ctrlM.encontrarCodigo(pegarFabricante(cbTeclado),"teclado",pegarDescricao(cbTeclado)));
        m.setCod_gabinete(ctrlM.encontrarCodigo(pegarFabricante(cbGabinete),"gabinete",pegarDescricao(cbGabinete)));
