    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        d.setCodigo(Integer.parseInt(edtCodigo.getText()));
        d.setTipo(edtTipo.getText());
        int resp=JOptionPane.showConfirmDialog(null,"Deseja realmente alterar os dados do defeito "+d.getCodigo()+" ?");
        if(resp==JOptionPane.YES_OPTION){
            int erro=ctrlD.alterarDefeito (d);
            if(erro==0){
                JOptionPane.showMessageDialog(null,"AlteraÃ§Ã£o concluÃ­da com Ãªxito");
                limpar();
                resetar();
            }else
            JOptionPane.showMessageDialog(null,"Falha na alteraÃ§Ã£o");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int cod=Integer.parseInt(edtCodigo.getText());
        int rc=ctrlD.verificar(cod);
        if (rc!=0){
        d.setCodigo(cod);
        int resp=JOptionPane.showConfirmDialog(null,"Deseja resalmente excluir o defeito "+d.getCodigo()+" ?");
        if (resp==JOptionPane.YES_OPTION){
            int erro=ctrlD.excluirDefeito (d);
            if(erro==0){
                JOptionPane.showMessageDialog(null,"ExclusÃ£o concluÃ­da com Ãªxito");
                limpar();
                resetar();
            }else
            JOptionPane.showMessageDialog(null,"Falha na exclusÃ£o");
        }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        int cod=Integer.parseInt(edtCodigo.getText());
        int rc=ctrlD.consultar(cod, d);
        if (rc!=0)
        JOptionPane.showMessageDialog(null,"Defeito de cÃ³digo "+cod+" nÃ£o encontrado !");
        else {
            edtTipo.setText(d.getTipo());
            edtCodigo.setText(String.valueOf(d.getCodigo()));
            liberar();
            edtCodigo.setEnabled(false);
            btnIncluir.setEnabled(false);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

