    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String nome;
        if ("".equals(edtNome.getText()))
        JOptionPane.showOptionDialog(null,"Por favor, preencha o campo","Campo Vazio",JOptionPane.DEFAULT_OPTION,
            JOptionPane.WARNING_MESSAGE,null, null, null);
        else{
            nome = edtNome.getText();
            int resp=JOptionPane.showConfirmDialog(null,"Deseja realmente alterar o seu nome ?");
            if(resp==JOptionPane.YES_OPTION){                
                int erro=ctrlU.alterarNome(nome);
                if(erro==0){
                    JOptionPane.showMessageDialog(null,"Alteração concluída com êxito");
                }else
                    JOptionPane.showMessageDialog(null,"Falha na alteração");
                }
            }        
    }//GEN-LAST:event_btnAlterarActionPerformed

